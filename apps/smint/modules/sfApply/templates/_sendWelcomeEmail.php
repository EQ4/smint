Welcome to SEARCH by Sound portal.

SEARCH by Sound is an entirely new approach for audio search. While SEARCH by Sound still allows users to run a text-based search, our technology analyzes and labels sounds to provide better results faster. The results are ranked by degree of acoustic similarity including tempo, rhythmic feel and genre.

Users can also perform sound similarity searches for short sequences by using sample sequences. This gives even more specific search results. 

